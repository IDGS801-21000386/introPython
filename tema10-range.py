numeros = range(5) # [0,1,2,3,4]
print(numeros)
print(type(numeros)) # DEVUELVE QUE EL TIPO ES "range"
print(numeros[3]) # DEL RANGO  [0,1,2,3,4] DEVUELVE EL "3" INDICE

numeros2 = range(4,10) # DEVUELVE DEL 4 AL 9
numeros3 = range(1, 10, 2) # EL RANGO ES DEL 1 AL 10 PERO LO CUENTA DE "2" EN "2"
print(numeros2)
print(numeros3)
